jaxon.dialogs.notify = {
    success: (content, title) => $.notify(content, {className: 'success', position: "top center"}),
    info: (content, title) => $.notify(content, {className: 'info', position: "top center"}),
    warning: (content, title) => $.notify(content, {className: 'warn', position: "top center"}),
    error: (content, title) => $.notify(content, {className: 'error', position: "top center"}),
};
